 DATABASE_ROLE ROLE, SWITCHOVER_STATUS 
FROM V$DATABASE;


prompt SOURCE MAX ARCHIVED ARL
SELECT MAX(SEQUENCE#), THREAD# FROM V$ARCHIVED_LOG
WHERE RESETLOGS_CHANGE# = (SELECT MAX(RESETLOGS_CHANGE#) FROM V$ARCHIVED_LOG)
GROUP BY THREAD#;

